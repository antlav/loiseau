    public void recupInfosPriseDeMesure() {
        // Récupere et ajoute dans les textfiels la date, le nom, l'adresse, le téléphone
        txtDate.setText(sheet.getCell(2, 2).getContents());
        txtNom.setText(sheet.getCell(2, 3).getContents());
        txtAdresse.setText(sheet.getCell(2, 4).getContents());
        txtTel.setText(sheet.getCell(2, 5).getContents());
    }

    public void recupArticle() {
         // Parcourt tous les articles
        for (int i = 24; i < sheet.getRows(); i++) {
            Cell cell = sheet.getCell(0, i);
            if (cell.getContents() == "") {
                break;
            } else {
                // Créer un vecteur en y ajoutant le type, la largeur et la hauteur des articles
                vArticle = new Vector();
                vArticle.addElement(sheet.getCell(0, i).getContents());
                vArticle.addElement(sheet.getCell(1, i).getContents());
                vArticle.addElement(sheet.getCell(2, i).getContents());
                lesArticle.add(vArticle);
                // ajoute le vetceur vArticle au dtm
                dtm.addRow(vArticle);
                
            }
        }
    }
    public static Vector<Vector> openForm(Component cpt){
        lesArticle=null;
        importPriseDeMesure impor=new importPriseDeMesure();
        impor.setLocationRelativeTo(cpt);
        impor.setVisible(true);
        return lesArticle;
    }
