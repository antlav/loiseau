    private void cmdParcourirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdParcourirActionPerformed
        // TODO add your handling code here:
        
        // Ouverture de la boite de dialogue "Parcourir..."
        int returnVal = fc.showOpenDialog(this);
        
           // Evenement de la boite de dialogue "Parcourir..."
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            try {
                
                //Chargement du fichier Excel
                String url = fc.getSelectedFile().getAbsolutePath();
                txtURL.setText(url);
                fichierCharge = Workbook.getWorkbook(new File(url), ws);
                sheet = fichierCharge.getSheet(0);
                
                // Appelle de la fontion qui récupere l'entête du fichier Exel
                recupInfosPriseDeMesure();
                
                  // Appelle de la fontion qui récupere les articles
                recupArticle();

            } catch (IOException ex) {
                Logger.getLogger(importPriseDeMesure.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BiffException ex) {
                Logger.getLogger(importPriseDeMesure.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_cmdParcourirActionPerformed

